//OPERADORES DE ATRIBUICAO

//atruibuicao basica de valor
let valor1 = 100;
let valor2 = 20;

//adicionar e atrbuir
valor1 += valor2; //valor1 = 120

//subtrair e atrbuir
valor1 -= valor2; //valor1 = 80

//multiplicar e atrbuir
valor1 *= valor2; //valor1 = 2000

//dividir e atrbuir
valor1 /= valor2; //valor1 = 5

//calcular a sobra da divisao e atrbuir
valor1 += valor2; //valor1 = 0

//mesma coisa
valor1 += valor2;
valor1 = valor1 + valor2;

